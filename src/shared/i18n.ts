export function normalizeLanguage(value: unknown): AppLanguage {
  return typeof value === "string" && APP_LANGUAGE_CODES.has(value as AppLanguage)
    ? (value as AppLanguage)
    : DEFAULT_LANGUAGE;
}

export function htmlLang(language: AppLanguage): string {
  return LANGUAGE_OPTIONS.find((option) => option.code === language)?.htmlLang || "en";
}

export function createTranslator(
  language: AppLanguage,
): (key: TranslationKey, values?: Record<string, string | number>) => string {
  const dictionary = TRANSLATIONS[language] || TRANSLATIONS.en;
  return (key, values = {}) => {
    let text: string = dictionary[key] || TRANSLATIONS.en[key] || key;
    for (const [name, value] of Object.entries(values)) {
      text = text.replaceAll(`{${name}}`, String(value));
    }
    return text;
  };
}
