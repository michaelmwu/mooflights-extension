export type ContentTranslationKey = keyof (typeof CONTENT_TRANSLATIONS)["en"];

export function createContentTranslator(
  language: AppLanguage,
): (key: ContentTranslationKey, values?: Record<string, string | number>) => string {
  const dictionary = CONTENT_TRANSLATIONS[language] || CONTENT_TRANSLATIONS.en;
  return (key, values = {}) => {
    let text: string = dictionary[key] || CONTENT_TRANSLATIONS.en[key] || key;
    for (const [name, value] of Object.entries(values)) text = text.replaceAll(`{${name}}`, String(value));
    return text;
  };
}
